    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
      String str1 = evt.getActionCommand();
      if(str1.equals("Submit"));
      {
      jTextField5.setText(vote1);
       if ("Congress".equals(vote1)) {
                    congressVotes++;
                } else if ("BJP".equals(vote1)) {
                    bjpVotes++;
                } else if ("AAP".equals(vote1)) {
                    aapVotes++;
                }
                    jTextField6.setText("Vote Counts - Congress:    " + congressVotes + "    , BJP:    " + bjpVotes + "    , AAP:     " + aapVotes);

      }
      age =Integer.parseInt(jTextField1.getText());
      if (age<18)
      {
      jTextField5.setText("You are not Elligibe for Voting");
      }
       if ("Congress".equals(vote1)) {
                    congressVotes++;
                } else if ("BJP".equals(vote1)) {
                    bjpVotes++;
                } else if ("AAP".equals(vote1)) {
                    aapVotes++;
                }
                    jTextField6.setText("Vote Counts - Congress: " + congressVotes + ", BJP: " + bjpVotes + ", AAP: " + aapVotes);

    }//GEN-LAST:event_jButton1ActionPerformed

